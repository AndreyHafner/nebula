package org.eclipse.nebula.widgets.calendarcombo;

public class CalendarDateParseException extends Exception {

	private static final long	serialVersionUID	= 8637307656859889011L;

	public CalendarDateParseException() {
		super();
	}

	public CalendarDateParseException(String message, Throwable cause) {
		super(message, cause);
	}

	public CalendarDateParseException(String message) {
		super(message);
	}

	public CalendarDateParseException(Throwable cause) {
		super(cause);
	}

	
}
